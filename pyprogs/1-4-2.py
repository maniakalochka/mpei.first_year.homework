a = list(map(int, input('Введите массив A через пробел: ').split()))
print('Массив а: ', a)
arr_length = len(a)
c_1 = c_2 = m_1 = m_2 = 0
for i in range(arr_length) :
    if a[i] < 0:
        c_1 += a[i]
        m_1 += 1
    elif a[i] > 0:
        c_2 += a[i]
        m_2 += 1
print('Сумма отрицательных элементов = ', c_1)
print('Сумма положительных элементов = ', c_2)
print('Количество отрицательных элементов: ', m_1)
print('Кол-во положительных элементов: ', m_2)
